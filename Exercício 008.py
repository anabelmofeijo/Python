# conversor de medidas

medida = int (input("digite a medida em metros:  "))

print (f"A sua medida em Km é: {medida*0.001} ")

print (f"A sua medida em hm é: {medida*0.01} ")

print (f"A sua medida em dam é: {medida*0.1:.1f} ")

print (f"A sua medida em m é: {medida*1} ")

print (f"A sua medida em dm é: {medida*10}")

print (f"A sua medida em cm é: {medida*100} ")

print (f"A sua medida em mm é: {medida*1000}")